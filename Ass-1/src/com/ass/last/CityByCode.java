package com.ass.last;

public class CityByCode {

	
	public String getCityByCode(int number) {
 
		switch(number) {
		case 1: return " PUNE ";
 
		case 2: return "NOIDA";
 
		case 3: return " BENGALORE";
 
		case 4: return "DELHI";
 
		default: return "INVALIDCODE";
 
		
		}
 
	}

}
